package day11.inheritance.상속미사용;

// 학생 한명이 가져야 할 정보를 표현하기 위한 자료형
public class Student {
	public void 말한다() {
		System.out.println("말한다");
	}

	public void 먹는다() {
		System.out.println("먹는다");
	}

	public void 걷는다() {
		System.out.println("걷는다");
	}

	public void 잠잔다() {
		System.out.println("잠잔다");
	}

	public void 공부한다() {
		System.out.println("공부한다");
	}
}
